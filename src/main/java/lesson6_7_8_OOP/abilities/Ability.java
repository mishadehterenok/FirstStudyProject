package lesson6_7_8_OOP.abilities;

public class Ability {
    private String first;
    private String second;

    public Ability(String first, String second) {
        this.first = first;
        this.second = second;
    }
    public String getFirst() {
        return first;
    }
    public String getSecond() {
        return second;
    }
}






